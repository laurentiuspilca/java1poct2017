    private void adaugaVehicul() {
        String marca = jTextField1.getText();
        String culoare = jTextField2.getText();
        
        Vehicul v = new Vehicul();
        v.setMarca(marca);
        v.setCuloare(culoare);
        
        VehiculController.getInstance().adaugaVehicul(v);
        
        dispose();
    }

