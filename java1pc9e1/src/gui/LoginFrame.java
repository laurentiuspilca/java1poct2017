    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String username = jTextField1.getText();
        String password = new String(jPasswordField1.getPassword());
        
        User user = new User();
        user.setUsername(username);
        user.setPassword(password);
        user.setAdmin(jCheckBox1.isSelected());
        
        UserService service = new UserService();
        boolean rez = service.register(user);
        
        if (rez) {
            JOptionPane.showMessageDialog(this, "User was registered!");
        } else {
            JOptionPane.showMessageDialog(this, "User already exits!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = jTextField1.getText();
        String password = new String(jPasswordField1.getPassword());
        
        User user = new User();
        user.setUsername(username);
        user.setPassword(password);
        
        UserService service = new UserService();
        Optional<User> rez = service.login(user);
        
        if (rez.isPresent()) {
            new MainFrame(rez.orElse(null));
            dispose();
        } else {
           JOptionPane.showMessageDialog(this, "Wrong username or password!"); 
        }
    }//GEN-LAST:event_jButton1ActionPerformed

