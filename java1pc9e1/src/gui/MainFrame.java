    private void showBoxes() {
        model.clear();
        List<Box> boxes;
        BoxService service = new BoxService();
        
        if (user.isAdmin()) {
            boxes = service.getAll();
        } else {
            boxes = service.getForUser(user);
        }
        
        boxes.forEach(model::addElement);
    }
    
